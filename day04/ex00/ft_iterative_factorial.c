int		ft_iterative_factorial(int nb)
{
	int i;

	i = nb;
	if (i < 0 || i >= 13)
		return (0);
	nb = 1;
	while (i >= 1)
	{
		nb *= i;
		i--;
	}
	return (nb);
}
